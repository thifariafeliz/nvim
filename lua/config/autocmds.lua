-- tabstop: quantos espaços um TAB ocupa visualmente
-- softtabstop: quantos espaços são usados quando você pressiona tab no modo inserção
-- shiftwidth: quantos espaços são usados ao indentar com << >>
-- expandtab: se true, insere espaços em vez de caractere de tab
--

vim.api.nvim_create_autocmd("FileType", {
  pattern = "javascript, typescript, html, css, lua",
  callback = function()
    vim.bo.shiftwidth = 2
    vim.bo.tabstop = 2
    vim.bo.softtabstop = 2
    vim.bo.expandtab = true
  end,
})

